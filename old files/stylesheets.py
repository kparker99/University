def large_button_style(self):
    """large buttons (ex. 'login', 'close', etc.)"""
    style = "background-color: rgb(180, 145, 76);\n" \
            "font: 12pt \"Montserrat SemiBold\";\n" \
            "color: white;\n" \
            "border-style: outset;\n" \
            "border-width: 2px;\n" \
            "border-radius: 5px;"
    return style

